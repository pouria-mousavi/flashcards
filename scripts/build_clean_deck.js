let finalOutput = '';

sourceFiles.forEach(file => {
    try {
        const filePath = path.join(__dirname, '..', file);
        if (fs.existsSync(filePath)) {
            console.log(`Processing ${file}...`);
            const content = fs.readFileSync(filePath, 'utf-8');
            const lines = content.split('\n');
            let skipCard = false;
            
            for (let i = 0; i < lines.length; i++) {
                const line = lines[i].trim();
                
                if (line.includes('|')) {
                    // New card header
                    const parts = line.split('|');
                    const front = parts[0].trim();
                    if (blacklist.has(front)) {
                        console.log(`Skipping blacklisted: ${front}`);
                        skipCard = true;
                    } else {
                        skipCard = false;
                        finalOutput += line + '\n';
                    }
                } else if (!skipCard) {
                    // Start of card or metadata line for allowed card
                    finalOutput += line + '\n';
                }
            }
            finalOutput += '\n'; // Spacer
        } else {
             console.warn(`File not found: ${file}`);
        }
    } catch (e) {
        console.error(`Error reading ${file}:`, e);
    }
});

fs.writeFileSync(path.join(__dirname, '../flashcards.txt'), finalOutput);
console.log("Rebuilt flashcards.txt successfully.");
